var gameIdUsed = make(map[string]bool)

func initGameIds() {
	for _, gameId := range gameIdList {
		gameIdUsed[gameId] = false
	}
}

func createGameId() (gameId string) {
	rand.Seed(time.Now().UnixNano())
	gameId = gameIdList[rand.Intn(len(gameIdList))]
	for gameIdUsed[gameId] {
		gameId = gameIdList[rand.Intn(len(gameIdList))]
	}
	gameIdUsed[gameId] = true
	return
}
